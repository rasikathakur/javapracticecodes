class Relationalnop{
	public static void main(String args[]){
		System.out.println("Relational Operators:");
		int num1 = 30;
		int num2 = 4;
		int num3 = 30;
		
		System.out.println(num1+" is greater than(>) "+num2 +": "+(num1 > num2));
		System.out.println(num1+" is greater than(>)  "+num3 +": "+(num1 > num3));

		
		System.out.println(num1+" is less than(<) "+num2 +": "+(num1 < num2));
		System.out.println(num1+" is less than(<) "+num3 +": "+(num1 < num3));

		
		System.out.println(num1+" is greater than equal to(>=)  "+num2 +": "+(num1 >= num2));
		System.out.println(num1+" is greater than equal to(>=)  "+num3 +": "+(num1 >= num3));

		
		System.out.println(num1+" is less than equal to(<=) "+num2 +": "+(num1 <= num2));
		System.out.println(num1+" is less than equal to(<=) "+num3 +": "+(num1 <= num3));

		
		System.out.println(num1+" is equal to(==) "+num2 +": "+(num1 == num2));
		System.out.println(num1+" is equal to(==) "+num3 +": "+(num1 == num3));

		
		System.out.println(num1+" is not equal to(!=) "+num2 +": "+(num1 != num2));
		System.out.println(num1+" is not equal to(!=) "+num3 +": "+(num1 != num3));
		
		
	}
}